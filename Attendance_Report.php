        function updateDates() {
            var currentDate = new Date();
            var currentMonth = currentDate.getMonth() + 1;
            var currentYear = currentDate.getFullYear();
            var Selected_Month = document.getElementById('Selected_Month').value;
            var Selected_Year = document.getElementById('Selected_Year').value;

            var firstDay = new Date(Selected_Year, Selected_Month - 1, 1);
            var lastDay = new Date(Selected_Year, Selected_Month, 0);

            var From_Date = document.getElementById('From_Date');
            var To_Date = document.getElementById('To_Date');

            From_Date.value = moment(firstDay).format('YYYY-MM-DD');

            if(Selected_Month == currentMonth && Selected_Year == currentYear){
                To_Date.value = moment(currentDate).format('YYYY-MM-DD');
            }else{
                To_Date.value = moment(lastDay).format('YYYY-MM-DD');
            }
            
            View_Attendance_Report(3,"Attendance_Report");
        }

        $(document).ready(function() {
            var currentDate = new Date();
            var currentMonth = currentDate.getMonth() + 1;
            var currentYear = currentDate.getFullYear();

            $('#Selected_Month').val(currentMonth);
            $('#Selected_Year').val(currentYear);

            updateDates();
        });
